#swapping using third variable

num1=10
num2=20

print("Before Swapping")
print("Number 1 is",num1)
print("Number 2 is",num2)

# num1=num2 (num1=20)
# num2=num1 (num2=20)

temp=num1  #temp=10
num1=num2  #num1=20
num2=temp  #num2=10

print("After Swapping")
print("Number 1 is",num1)
print("Number 2 is",num2)

#using third variable swapping method