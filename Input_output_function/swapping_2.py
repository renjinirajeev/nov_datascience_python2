#Swapping Using arithemetic operation

num1=10
num2=20
print("Before Swapping")
print("Number 1 is",num1)
print("Number 2 is",num2)

#no extra no or no extra variable

num1=num1+num2     #num1=10+20=30
num2=num1-num2     #num2=30-20=10
num1=num1-num2     #num1=30-10=20

print("After Swapping")
print("Number 1 is",num1)
print("Number 2 is",num2)