#Relational Operators

#<,>,<=,>=,==,!=

#used for comparison

#output: True or False

num1=10
num2=20
print(num1<num2) #10<20
print(num1>num2)  #10>20
print(num1<=num2) #10<=20
print(num1>=num2) #10>=20
print(num1==num2) #10==20
print(num1!=num2) #10!=20

#True or False

# true==> 1
# false==> 0

# True>False    1>0
# >>> True

# True<False    1<0
# >>> False
